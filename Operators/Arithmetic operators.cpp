#include <iostream>
using namespace std;

int main () {
	
	int a = 5;
	int b = 6;
	
	// Addition of two numbers.
	cout << "a + b : " << a + b << endl;
	
	// Subtraction of two numbers.
	cout << "a - b : " << a - b << endl;
	
	// Multiplication of two numbers;
	cout << "a * b : " << a * b << endl;
	
	// Division of two numbers.
	cout << "a / b : " << a / b << endl;
	
	// Modulous of two number.
	cout << "a % b : " << a % b << endl;
	
	cout << endl;
	
	// int divided by int gives int value.
	cout << 5 / 2 << endl;
	
	// float divided by int gives float value.
	cout << 5.5 / 2 << endl;
	
	// int divided by float gives float value.
	cout << 5 / 2.4 << endl;
	
	// double divided by int gives double value.
	cout << 5.23 / 2 << endl;
	
	// int divided by double gives double value.
	cout << 5 / 2.23 << endl;
	
	
}