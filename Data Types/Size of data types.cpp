#include <iostream>
using namespace std;

int main () {
	
	int a ;
	
	cout << "Size of int is : " << sizeof(a) << " byte" << endl;
	
	bool b ;
	
	cout << "Size of bool is : " << sizeof(b) << " byte" << endl;
	
	char c ;
	
	cout << "Size of char is : " << sizeof(c) << " byte" << endl;
	
	double d ;
	
	cout << "Size of double is : " << sizeof(d) << " byte" << endl;
	
	float f ;
	
	cout << "Size of float is : " << sizeof(f) << " byte" << endl;
}